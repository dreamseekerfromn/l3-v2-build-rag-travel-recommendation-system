
CREATE TABLE mountains_vs_beaches(
    id SERIAL PRIMARY KEY,
    Age INT,
    Gender VARCHAR(10),
    Income INT,
    Education_Level VARCHAR(20),
    Travel_Frequency INT,
    Preferred_Activities VARCHAR(20),
    Vacation_Budget INT,
    Location VARCHAR(20),
    Proximity_to_Mountains INT,
    Proximity_to_Beaches INT,
    Favorite_Season VARCHAR(6),
    Pets INT,
    Environmental_Concerns INT,
    Preference INT
)